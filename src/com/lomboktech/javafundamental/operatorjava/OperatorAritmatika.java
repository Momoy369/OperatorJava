package com.lomboktech.javafundamental.operatorjava;

public class OperatorAritmatika {

    public static void main(String[] args){
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5+1;
        System.out.println("Hasil penjumlahan 5+1 adalah: " +hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 10-8;
        System.out.println("Hasil pengurangan 10-8 adalah: " +hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Perkalian");
        int hasilPerkalian = 10*10;
        System.out.println("Hasil perkalian 10*10 adalah: " +hasilPerkalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 10 / 10;
        System.out.println("Hasil pembagian 10/10 adalah: " +hasilPembagian);
        System.out.println();

        System.out.println("Operasi Habis Bagi");
        int hasilSisa = 8%2;
        System.out.println("Hasil habis bagi 8%2 adalah: " +hasilSisa);
        int hasilSisaLain = 9%2;
        System.out.println("Hasil bagi 9%2 adalah: " +hasilSisaLain);
        System.out.println();
    }
}
